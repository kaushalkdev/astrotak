class BirthDetails {
  final int dobYear;
  final int dobMonth;
  final int dobDay;
  final int tobHour;
  final int tobMin;
  final String meridiem;

  BirthDetails({
    required this.dobYear,
    required this.dobMonth,
    required this.dobDay,
    required this.tobHour,
    required this.tobMin,
    required this.meridiem,
  });
}
